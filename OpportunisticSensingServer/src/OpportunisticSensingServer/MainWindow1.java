    private void connectBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_connectBtnMouseClicked

        if(!this.listeningConnections)
        { //Can only listen to connections if it isn't already listening to connections
            this.listeningConnections = true;
            this.connectBtn.setEnabled(!this.listeningConnections);
            ct = new ConnectionThread(this);
            ct.start();
        }
    }//GEN-LAST:event_connectBtnMouseClicked

    private void cancelBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelBtnMouseClicked
            if(ct != null && ct.isAlive())
            {
                ct.interrupt();
            }
            System.exit(-1);
    }//GEN-LAST:event_cancelBtnMouseClicked

